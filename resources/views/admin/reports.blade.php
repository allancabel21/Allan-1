<script>
function openGenerateReportModal() {
    document.getElementById('generateReportModal').classList.remove('hidden');
}

function closeGenerateReportModal() {
    document.getElementById('generateReportModal').classList.add('hidden');
}

function viewReport(reportId) {
    // Load report details via AJAX
    fetch(`/admin/reports/${reportId}`)
        .then(response => response.json())
        .then(data => {
            document.getElementById('reportContent').innerHTML = `
                <div class="bg-gray-50 p-4 rounded-lg">
                    <h4 class="font-semibold text-gray-900 mb-2">${data.title}</h4>
                    <p class="text-sm text-gray-600 mb-4">${data.description || 'No description provided'}</p>
                    <div class="bg-white p-4 rounded border">
                        <pre class="text-sm text-gray-700 whitespace-pre-wrap">${JSON.stringify(data.data, null, 2)}</pre>
                    </div>
                </div>
            `;
            document.getElementById('reportDetailsModal').classList.remove('hidden');
        })
        .catch(error => {
            console.error('Error loading report:', error);
            alert('Error loading report details');
        });
}

function closeReportDetailsModal() {
    document.getElementById('reportDetailsModal').classList.add('hidden');
}

function downloadReport(reportId) {
    // Create download link
    const link = document.createElement('a');
    link.href = `/admin/reports/${reportId}/download`;
    link.download = `report_${reportId}.json`;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}

function deleteReport(reportId) {
    document.getElementById('confirmDeleteReport').onclick = function() {
        fetch(`/admin/reports/${reportId}`, {
            method: 'DELETE',
            headers: {
                'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content'),
                'Content-Type': 'application/json',
            },
        })
        .then(response => response.json())
        .then(data => {
            if (data.success) {
                location.reload();
            } else {
                alert('Error deleting report');
            }
        })
        .catch(error => {
            console.error('Error deleting report:', error);
            alert('Error deleting report');
        });
        
        closeDeleteReportModal();
    };
    
    document.getElementById('deleteReportModal').classList.remove('hidden');
}

function closeDeleteReportModal() {
    document.getElementById('deleteReportModal').classList.add('hidden');
}

// Close modals when clicking outside
document.addEventListener('click', function(event) {
    const generateModal = document.getElementById('generateReportModal');
    const detailsModal = document.getElementById('reportDetailsModal');
    const deleteModal = document.getElementById('deleteReportModal');
    
    if (event.target === generateModal) {
        closeGenerateReportModal();
    }
    if (event.target === detailsModal) {
        closeReportDetailsModal();
    }
    if (event.target === deleteModal) {
        closeDeleteReportModal();
    }
});
</script>
